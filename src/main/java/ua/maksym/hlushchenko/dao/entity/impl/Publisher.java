package ua.maksym.hlushchenko.dao.entity.impl;

import lombok.*;
import ua.maksym.hlushchenko.orm.annotations.*;

import java.util.List;

@Getter
@Setter
@ToString(exclude = "books")
@EqualsAndHashCode(exclude = {"name", "books"})
@Table("publisher")
public class Publisher {
    @Id(value = "id", autoGenerated = true)
    private int id;

    @Column("name")
    private String name;

    @OneToMany(genericType = Book.class,
            cascadeType = {CascadeType.SAVE, CascadeType.UPDATE},
            mappedBy = "publisher")
    private List<Book> books;
}
